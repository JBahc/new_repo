package a018num;

public class a18_character {
	
	public static void main(String[] args) {
		System.out.println("22 0702 토요일."
				+ "\n생활코딩 19번 강의, 문자열");
		System.out.println(' ');
		System.out.println(' ');
		System.out.println('생');
		System.out.println("문자Character는 한 글자, 1음절을 말하며 작은 따옴표로 감싸야 한다.");
		System.out.println("생활코딩");
		System.out.println("문자열String은 여러 문자의 결합을 말하며 큰 따옴표로 감싸야 한다.");
		System.out.println(' ');
		System.out.println("문자열을 작은따옴표로 감싸거나");
		System.out.println("문자를 큰 따옴표로 감싸면 에러 메세지가 뜬다.");
		System.out.println("작성 중에도 그러한 오류를 확인할 수 있다.");
		System.out.println("왼쪽줄에 작고 빨간 엑스가 뜨고, 하단창 Problems에 Errors항목이 뜨면서 에러에 대한 자세한 설명이 출력된다.");
		System.out.println(' ');
		System.out.println("+, -, *, /,... 이러한 것들을 연산자라 부른다.");
		System.out.println("이것은 문자에도 적용할 수 있다.");
		System.out.println("예:"+" 생활코딩"+" 입니다.");
		System.out.println(' ');
		System.out.println("아래는 1+1에 따옴표를 어디에 어떻게 썼냐에 따라 달라지는 결과물의 예시이다.");
		System.out.println("각 문자열의 차이를 확인할 수 있다.");
		System.out.println(1+1);
		System.out.println("1+1");
		System.out.println("1"+"1");
		System.out.println(' ');
		System.out.println("만약 문자열 안에 따옴표를 써서 화면에 출력되게 하고 싶다면");
		System.out.println("화면에 기호로 출력되어야 할 따옴표 앞에 역슬래시\\ 혹은 원화표시를 넣으면 된다.");
		System.out.println(" \"1+1\" ");
		System.out.println("이러한 기법을 escape라고 한다.");
		System.out.println("큰따옴표가 가진 문법적인 역할에서 도망쳐 문자로 인식되도록 한다는 의미이다.");
		System.out.println("\n만약 줄바꿈을 하고 싶다면? \n\\n을 입력하면 된다. \n\\");
	}

}
